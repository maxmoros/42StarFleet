#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t		i;

	if (!s)
		return (NULL);
	while (*s == ' ' || *s == '\n' || *s == '\t')
		s++;
	i = -1;
	while (s[++i])
		;
	if (i)
		while (s[--i] == ' ' || s[i] == '\n' || s[i] == '\t')
			;
	return (ft_strndup((char*)s, ++i));
}
