#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*tmp;

	tmp = s1;
	if (*tmp)
		while (*++tmp)
			;
	n++;
	while (*s2 && --n)
		*tmp++ = *s2++;
	*tmp = '\0';
	return (s1);
}
