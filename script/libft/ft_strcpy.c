#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*tmp;

	tmp = dst;
	if ((*tmp = *src))
		while ((*++tmp = *++src))
			;
	*tmp = '\0';
	return (dst);
}
